// Create a variable name "password"
let password = 9;

// Check conditional statements
if (password == 8) {
    console.log("Welcome!");
} else if (password <= 8) {
    console.log(`${password} is to short.`);
} else if (password >= 8) {
    console.log(`${password} is too long.`);
} else {
    console.log("Please enter a password.");
};
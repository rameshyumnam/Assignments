// Create a variable "Fruits" and using switch case examine different fruits.
// Always add default values in switch case.

// Variable fruits
let fruits = "Lemon";

// Switch cases
switch (fruits) {
    case "Apple":
        console.log(`${fruits} is my favourite fruit.`);
        break;

    case "Banana":
        console.log(`${fruits} is sweet.`);
        break;

    case "Grapes":
        console.log(`${fruits} is beautiful and amazing fruit.`);
        break;

    case "Orange":
        console.log(`${fruits} is the name of a colour too.`);
        break;

    case "Mango":
        console.log(`${fruits} is like an S-shaped fruit.`);
        break;

    case "Strawberry":
        console.log(`${fruits} is Red in colour with black dots around its body.`);
        break;

    case "Pineapple":
        console.log(`${fruits} leaves are dangerous but its fruits is sweet and tasty.`);
        break;

    case "Lemon":
        console.log(`${fruits} is a sour fruit. Its sourness is 100 out of 10.`);
        break;

    default:
        console.log("I have never heard of this Fruit.");
}
// check equality operators ==, ===, !=, !==, <, >, <=, >=.
let firstFavNumber = 10;
let secondFavNumber = "10";

// loose equal ==
console.log(firstFavNumber == secondFavNumber);

// strict equal ===
console.log(firstFavNumber === secondFavNumber);

// loose non equal !=
console.log(firstFavNumber != secondFavNumber);

// strict non equal !==
console.log(firstFavNumber !== secondFavNumber);

// less than <
console.log(firstFavNumber < secondFavNumber);

// greater than >
console.log(firstFavNumber > secondFavNumber);

// less than or equal to <=
console.log(firstFavNumber <= secondFavNumber);

// greater than or equal to >=
console.log(firstFavNumber >= secondFavNumber);